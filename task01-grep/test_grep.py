def test_search_needle_in_line():
    parser = argparse.ArgumentParser()
    parser.add_argument('needle', type=str)
    parser.add_argument('files', nargs='*')
    parser.add_argument('-E', dest='regex', action='store_true')
    parser.add_argument('-c', dest='counter', action='store_true')
    parser.add_argument('-i', dest='ignore', action='store_true')
    parser.add_argument('-v', dest='inverse', action='store_true')
    parser.add_argument('-x', dest='full_match', action='store_true')
    parser.add_argument('-l', dest='has_lines', action='store_true')
    parser.add_argument('-L', dest='no_lines', action='store_true')

    args = parser.parse_args(['tes', '-E'])
    assert grep.search_needle_in_line('test?', args)

    args = parser.parse_args(['tes -E'])
    assert not grep.search_needle_in_line('test?', args)

    args = parser.parse_args(['test'])
    assert not grep.search_needle_in_line('tes?', args)

    args = parser.parse_args(['tes?'])
    assert grep.search_needle_in_line('tes?to', args)


def helper(check, capsys):
    out, err = capsys.readouterr()
    assert err == ''
    assert out == check


def test_print_asked_string(capsys):
    parser = argparse.ArgumentParser()
    parser.add_argument('needle', type=str)
    parser.add_argument('files', nargs='*')
    parser.add_argument('-E', dest='regex', action='store_true')
    parser.add_argument('-c', dest='counter', action='store_true')
    parser.add_argument('-i', dest='ignore', action='store_true')
    parser.add_argument('-v', dest='inverse', action='store_true')
    parser.add_argument('-x', dest='full_match', action='store_true')
    parser.add_argument('-l', dest='has_lines', action='store_true')
    parser.add_argument('-L', dest='no_lines', action='store_true')

    args = parser.parse_args(['', '-c'])
    grep.print_asked_string([], args, '')
    helper('0\n', capsys)

    args = parser.parse_args(['', '-c'])
    grep.print_asked_string([1, 2, 3, 4, 5], args, '')
    helper('5\n', capsys)


def test_integrate_stdin_grep(monkeypatch, capsys):
    monkeypatch.setattr('sys.stdin', io.StringIO(
        'pref needle?\nneedle? suf\nthe needl\npref needle? suf'))
    grep.main(['needle?'])
    out, err = capsys.readouterr()
    assert err == ''
    assert out == 'pref needle?\nneedle? suf\npref needle? suf\n'


